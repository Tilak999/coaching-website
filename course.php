                        <p>In September 2013, the IIT Council approved the decision of the Joint Admission Board to continue with the two-phase JEE pattern ("Main" followed by "Advanced") for IITs in 2014. Joint Seat Allocation Authority (JoSAA) conducted the joint admission process for a total of 19 IITs, 32 NITs, 18 IIITs and 19 other Government Funded Technical Institutes (GFTIs).</p>

